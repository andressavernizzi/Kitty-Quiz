	public void Iniciar() {
		
		System.out.println("\n\nPress 1 to Start\nPress 2 to Exit");
		Scanner scanner = new Scanner(System.in);
		
		while(menuLoop == true) {
			try {
			
			option = scanner.nextInt();
			if(option == 1) {
				menuLoop = false;
				Quiz quiz = new Quiz();
			    quiz.QuizKitty();
								
			}
			else if(option == 2) {
				menuLoop = false;
				break;
			}
			else{
				System.out.println("Type 1 or 2 only:");				
			}
			}
			catch (InputMismatchException a) {
	            System.out.println("Type 1 or 2 only:");
	            scanner.next();
			}
		}
		scanner.close();
	}
